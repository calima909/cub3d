#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	k;

	k = 0;
	if (s)
	{
		while (s[k] != '\0')
		{
			(*f)(k, &s[k]);
			k++;
		}
	}
}
