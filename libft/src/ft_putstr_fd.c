#include "libft.h"

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
}

int	ft_putstr_fd(char *s, int fd)
{
	int	k;

	k = 0;
	if (s)
	{
		while (s[k] != '\0')
		{
			write(fd, &s[k], 1);
			k++;
		}
	}
	return (k);
}
