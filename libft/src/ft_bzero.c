#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			k;
	unsigned char	*s_ch;

	s_ch = s;
	k = 0;
	while (k < n)
	{
		s_ch[k] = '\0';
		k++;
	}
	s = s_ch;
}
