#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	k;

	k = 0;
	while (s[k] != '\0')
	{
		if (s[k] == c)
			return ((char *)&s[k]);
		k++;
	}
	if (s[k] == c)
		return ((char *)&s[k]);
	return (NULL);
}
